public class Loops {
    public static void main(String[] args) {
        //for loop, while loop, Do...While Loop. Initiliaze the value int i=0; Then boolean expression, i < 10, true runs loop, if false ends the loop, updates i++ iterates
        //For loop: statements multiple times, steps
        //while loop: repeats a statement while given condition is true, conditoonal
        //Do while loop:
        for(int x = 10; x < 20; x = x++) {
            System.out.println("value of x : " + x);
            System.out.println("\n");
        }
    }
}
